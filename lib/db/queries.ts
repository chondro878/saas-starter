import { desc, and, eq, isNull } from 'drizzle-orm';
import { db } from './drizzle';
import { activityLogs, teamMembers, teams, users } from './schema';
import { createSupabaseServerClient } from '@/lib/supabase/server';

export async function getUser() {
  const supabase = await createSupabaseServerClient();
  
  const {
    data: { user: supabaseUser },
  } = await supabase.auth.getUser();

  if (!supabaseUser?.email) {
    return null;
  }

  const user = await db
    .select()
    .from(users)
    .where(and(eq(users.email, supabaseUser.email), isNull(users.deletedAt)))
    .limit(1);

  if (user.length === 0) {
    return null;
  }

  return user[0];
}

export async function getTeamByStripeCustomerId(customerId: string) {
  const result = await db
    .select()
    .from(teams)
    .where(eq(teams.stripeCustomerId, customerId))
    .limit(1);

  return result.length > 0 ? result[0] : null;
}

export async function updateTeamSubscription(
  teamId: number,
  subscriptionData: {
    stripeSubscriptionId: string | null;
    stripeProductId: string | null;
    planName: string | null;
    subscriptionStatus: string;
  }
) {
  await db
    .update(teams)
    .set({
      ...subscriptionData,
      updatedAt: new Date()
    })
    .where(eq(teams.id, teamId));
}

export async function getUserWithTeam(userId: number) {
  const result = await db
    .select({
      user: users,
      teamId: teamMembers.teamId
    })
    .from(users)
    .leftJoin(teamMembers, eq(users.id, teamMembers.userId))
    .where(eq(users.id, userId))
    .limit(1);

  return result[0];
}

export async function getActivityLogs() {
  const user = await getUser();
  if (!user) {
    throw new Error('User not authenticated');
  }

  return await db
    .select({
      id: activityLogs.id,
      action: activityLogs.action,
      timestamp: activityLogs.timestamp,
      ipAddress: activityLogs.ipAddress,
      userName: users.name
    })
    .from(activityLogs)
    .leftJoin(users, eq(activityLogs.userId, users.id))
    .where(eq(activityLogs.userId, user.id))
    .orderBy(desc(activityLogs.timestamp))
    .limit(10);
}

export async function getTeamForUser() {
  const user = await getUser();
  if (!user) {
    return null;
  }

  const result = await db.query.teamMembers.findFirst({
    where: eq(teamMembers.userId, user.id),
    with: {
      team: {
        with: {
          teamMembers: {
            with: {
              user: {
                columns: {
                  id: true,
                  name: true,
                  email: true
                }
              }
            }
          }
        }
      }
    }
  });

  return result?.team || null;
}

// Card Credits Management
export async function addCardCredits(teamId: number, credits: number) {
  const team = await db.select().from(teams).where(eq(teams.id, teamId)).limit(1);
  
  if (team.length === 0) {
    throw new Error('Team not found');
  }

  const currentCredits = team[0].cardCredits || 0;
  
  await db
    .update(teams)
    .set({
      cardCredits: currentCredits + credits,
      updatedAt: new Date()
    })
    .where(eq(teams.id, teamId));
}

export async function deductCardCredit(teamId: number) {
  const team = await db.select().from(teams).where(eq(teams.id, teamId)).limit(1);
  
  if (team.length === 0) {
    throw new Error('Team not found');
  }

  const currentCredits = team[0].cardCredits || 0;
  
  if (currentCredits < 1) {
    throw new Error('Insufficient card credits');
  }

  await db
    .update(teams)
    .set({
      cardCredits: currentCredits - 1,
      updatedAt: new Date()
    })
    .where(eq(teams.id, teamId));
}

export async function getTeamCardCredits(teamId: number) {
  const team = await db.select().from(teams).where(eq(teams.id, teamId)).limit(1);
  
  if (team.length === 0) {
    return 0;
  }

  return team[0].cardCredits || 0;
}
